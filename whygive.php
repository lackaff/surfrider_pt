<p>We are ALL responsible for the upkeep and protection of the community’s beautiful coastline. Ericeira is now a <a href="http://www.savethewaves.org/world_surfing_reserves" target="_blank">World Wave Reserve</a> heritage site. Whether someone has lived in Ericeira for decades or is just visiting it for the first time, it’s probably obvious just how beautiful this place is. The Portuguese economy thrives on tourism from local and international visitors alike, so keeping the country and its waters clean is an absolute necessity.</p>

<p>But for those who live in the area, a clean environment does more than just boost the tourism industry. It helps protect the resources people rely on to survive and enjoy their lives. The use of plastic  has risen in recent years, and traces are now found in local wildlife and in some percentage of the food caught and consumed locally.</p>

<h5>Everyone plays a role</h5>

<p>Local fisherman, who make their livelihood from the waters of Ericeira, unintentionally hurt their industry’s future by choosing to use plastics and other materials to haul in their catch.</p>

<p>Locals and tourists inadvetertedly harm the community they care greatly for when leaving their rubbish out in plain sight.</p>

<p>Even surfers, who arguably love and enjoy the water and beaches of Ericeira as much as any group of people, unintentionally introduce petroleum into the ecosystem with their wetsuits and boards.</p>

